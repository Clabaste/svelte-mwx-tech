<h1>Svelte</h1>
<p>
    Svelte ist als privates Projekt von einem Entwickler namens
    Rich Harris gestartet und wird mittlerweile von seinem Arbeitgeber, der Firma  <a href="https://vercel.com/">Vercel</a>  unterstützt.
</p>
<p>
    Svelte ermöglicht, wie React, Vue, Angular und Konsorten, 
    die Entwicklung von Single-Page-Applikationen und nutzt hierfür ebenfalls Komponenten.
</p>
<p>
    Der Aufbau einer Komponente ist ähnlich wie bei Vue. 
    Script, HTML-Template und Styles sind in der selben Datei und es gibt die Möglichkeit, über Slots
    den inneren Content zu kontrollieren.
</p>
<h2>Uterschiede zu anderen Frameworks</h2>
<ul>
    <li>
        Build-Prozess: Svelte liefert keine Runtime aus, sondern ist ein Compiler. (Weniger Overhead). <br>
        Kurz und gut erklärt wird das <a href="https://b-nova.com/home/content/is-svelte-the-next-big-frontend-trend">hier</a> 
    </li>
    <li>Svelte benutzt kein virtuelles DOM</li>
</ul>
<h2>Nicht neu, aber sehr gut gemacht</h2>
